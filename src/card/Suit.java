package card;

/**
 * Enumerated type representing the suits of French playing cards.
 * 
 * @author Brodie Robertson
 *
 */
public enum Suit 
{
	/**
	 * The suit clubs.
	 */
	CLUBS,
	/**
	 * The suit spades.
	 */
	SPADES,
	/**
	 * The suit hearts.
	 */
	HEARTS,
	/**
	 * The suit diamonds.
	 */
	DIAMONDS
}
